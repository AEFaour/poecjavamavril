package fr.poecjava.javase.type.primitifs;

public class TypeBoolean {

	public static void main(String[] args) {
		System.out.println("========Type Boolean=======\n");
		
		boolean vrai = true; 
		
		boolean faux = false;
		
		System.out.printf("vrai = %s\n", vrai);
		System.out.printf("faux = %s\n", faux);

	}

}

// Le type boolean prend 2 valeurs : true & false
