package fr.poecjava.javase.operateurs;

public class OperateurArithmetique {

	public static void main(String[] args) {
	int a = 8; 
	int b = 3;

	System.out.printf("Addition = %s\n", a + b);
	System.out.printf("Soutraction = %s\n", a - b);
	System.out.printf("Multiplication = %s\n", a * b);
	System.out.printf("Division = %s\n", a / b);
	System.out.printf("Modolo = %s\n", a % b);
	}

}
